parseInput = do 
    p1 <- (,) <$> readInt <*> readInt
    p2 <- (,) <$> readInt <*> readInt
    n <- readInt
    circles <- replicateM n (Circle <$> readInt <*> readInt <*> readInt)
    return (p1, p2, circles)
  where
    readInt = state $ fromJust . BS.readInt . BS.dropWhile isSpace
    readInteger = state $ fromJust . BS.readInteger . BS.dropWhile isSpace
    readString = state $ BS.span (not . isSpace) . BS.dropWhile isSpace
    readLine = state $ BS.span (not . isEoln) . BS.dropWhile isEoln
    isEoln ch = ch == '\r' || ch == '\n'

data Circle = Circle Int Int Int

main = print =<< solve . evalState parseInput <$> BS.getContents

inCircle :: (Int, Int) -> Circle -> Bool
inCircle (x', y') (Circle x y r) = (x'-x)^2 + (y'-y)^2 <= r^2

range x y | x <= y    = init [x..y]
          | otherwise = init $ reverse [y..x]

solve ((x1, y1), (x2, y2), circles) = length $ filter (not.inAnyCircle) border
  where
    inAnyCircle pt = any (pt `inCircle`) circles

    border = [(x, y1) | x <- range x1 x2] ++
             [(x2, y) | y <- range y1 y2] ++
             [(x, y2) | x <- range x2 x1] ++
             [(x1, y) | y <- range y2 y1]


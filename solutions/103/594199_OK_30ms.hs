parseInput = do 
    n <- readInteger
    k <- readInteger
    p <- readInt
    query <- replicateM p readInteger
    return (n, k, query)
  where
    readInt = state $ fromJust . BS.readInt . BS.dropWhile isSpace
    readInteger = state $ fromJust . BS.readInteger . BS.dropWhile isSpace
    readString = state $ BS.span (not . isSpace) . BS.dropWhile isSpace
    readLine = state $ BS.span (not . isEoln) . BS.dropWhile isEoln
    isEoln ch = ch == '\r' || ch == '\n'

main = putStr =<< solve . evalState parseInput <$> BS.getContents

solve (n, k, query) = map solvePosition query
  where
    solvePosition x
        | k == 0    = '.'
        | k == n    = 'X'
        | k == 1    = if x == n then 'X' else '.'
        | partLeft  = if x == left then 'X' else '.'
        | partA     = if (x - left) `mod` 2 == 0 then 'X' else '.'
        | otherwise = 'X'
      where
        partLeft = x <= left
        partA = (x - left) <= 2 * a

    minB = n `mod` 2

    -- 2 * a + b <= n - 2
    -- a + b + 1 == k

    a = ((n - 2) - (k - 1)) `min` (k - 1 - minB)
    b = k - 1 - a

    left = n - 2 * a - b


